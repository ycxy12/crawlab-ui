<template>
  <div class="home">
    <el-row class="row-overview-metrics">
      <el-col
        v-for="(m, i) in metrics"
        :key="i"
        :span="24 / Math.min(metrics.length, 4)"
      >
        <cl-metric
          v-track="{
              code: 'click_home',
              params: {
                metricName: m.name
              }
            }"
          :icon="m.icon"
          :title="m.name"
          :value="m.value"
          :clickable="!!m.path"
          :color="getColor(m)"
          @click="onMetricClick(m)"
        />
      </el-col>
    </el-row>
    <el-row class="row-line-chart">
      <cl-line-chart
        :config="dailyConfig"
        is-time-series
        label-key="date"
      />
    </el-row>
    <el-row class="row-pie-chart">
      <el-col :span="8">
        <cl-pie-chart
          :config="tasksByStatusConfig"
          label-key="status"
          value-key="tasks"
        />
      </el-col>
      <el-col :span="8">
        <cl-pie-chart
          :config="tasksByNodeConfig"
          label-key="node_name"
          value-key="tasks"
        />
      </el-col>
      <el-col :span="8">
        <cl-pie-chart
          :config="tasksBySpiderConfig"
          label-key="spider_name"
          value-key="tasks"
        />
      </el-col>
    </el-row>
  </div>
</template>

<script lang="ts">
import {defineComponent, onMounted, ref} from 'vue';
import useRequest from '@/services/request';
import dayjs from 'dayjs';
import {spanDateRange} from '@/utils/stats';
import {useRouter} from 'vue-router';
import {
  TASK_STATUS_CANCELLED,
  TASK_STATUS_ERROR,
  TASK_STATUS_FINISHED,
  TASK_STATUS_PENDING,
  TASK_STATUS_RUNNING
} from '@/constants/task';
import {getColorByKey} from '@/utils';

const {
  get,
} = useRequest();

export default defineComponent({
  name: 'Home',
  setup() {
    const router = useRouter();

    const dateRange = ref<DateRange>({
      start: dayjs().subtract(30, 'day'),
      end: dayjs(),
    });

    const metrics = ref<MetricMeta[]>([
      {
        name: 'views.home.metrics.nodes',
        icon: ['fa', 'server'],
        key: 'nodes',
        value: 0,
        path: '/nodes',
      },
      {
        name: 'views.home.metrics.projects',
        icon: ['fa', 'project-diagram'],
        key: 'projects',
        value: 0,
        path: '/projects'
      },
      {
        name: 'views.home.metrics.spiders',
        icon: ['fa', 'spider'],
        key: 'spiders',
        value: 0,
        path: '/spiders',
      },
      {
        name: 'views.home.metrics.schedules',
        icon: ['fa', 'clock'],
        key: 'schedules',
        value: 0,
        path: '/schedules',
      },
      {
        name: 'views.home.metrics.tasks',
        icon: ['fa', 'tasks'],
        key: 'tasks',
        value: 0,
        path: '/tasks',
      },
      {
        name: 'views.home.metrics.error_tasks',
        icon: ['fa', 'exclamation'],
        key: 'error_tasks',
        value: 0,
        path: '/tasks',
        color: (m: MetricMeta) => m.value > 0 ? getColorByKey('danger') : getColorByKey('success'),
      },
      {
        name: 'views.home.metrics.results',
        icon: ['fa', 'table'],
        key: 'results',
        value: 0,
        color: (m: MetricMeta) => m.value > 0 ? getColorByKey('success') : getColorByKey('info-medium'),
      },
      {
        name: 'views.home.metrics.users',
        icon: ['fa', 'users'],
        key: 'users',
        value: 0,
        path: '/users',
      },
    ]);

    const dailyConfig = ref<EChartsConfig>({
      dataMetas: [
        {
          key: 'tasks',
          name: 'views.home.metrics.tasks',
          yAxisIndex: 0,
        },
        {
          key: 'results',
          name: 'views.home.metrics.results',
          yAxisIndex: 1,
        },
      ],
      data: [],
      option: {
        title: {
          text: 'views.home.dailyConfig.title',
        },
        yAxis: [
          {name: 'views.home.metrics.tasks', position: 'left'},
          {name: 'views.home.metrics.results', position: 'right'},
        ],
        color: [
          getColorByKey('primary'),
          getColorByKey('success'),
        ],
      }
    });

    const tasksByStatusConfig = ref<EChartsConfig>({
      data: [],
      option: {
        title: {
          text: 'views.home.tasksByStatusConfig.title',
        },
      },
      itemStyleColorFunc: ({data}: any) => {
        const {name} = data;
        switch (name) {
          case TASK_STATUS_PENDING:
            return getColorByKey('primary');
          case TASK_STATUS_RUNNING:
            return getColorByKey('warning');
          case TASK_STATUS_FINISHED:
            return getColorByKey('success');
          case TASK_STATUS_ERROR:
            return getColorByKey('danger');
          case TASK_STATUS_CANCELLED:
            return getColorByKey('info-medium');
          default:
            return 'red';
        }
      }
    });

    const tasksByNodeConfig = ref<EChartsConfig>({
      data: [],
      option: {
        title: {
          text: 'views.home.tasksByNodeConfig.title',
        },
      }
    });

    const tasksBySpiderConfig = ref<EChartsConfig>({
      data: [],
      option: {
        title: {
          text: 'views.home.tasksBySpiderConfig.title',
        },
      }
    });

    const getOverview = async () => {
      // TODO: filter by date range?
      // const {start, end} = dateRange.value;
      const res = await get(`/stats/overview`);
      metrics.value.forEach(m => {
        m.value = res?.data[m.key];
      });
    };

    const getDaily = async () => {
      // TODO: filter by date range?
      const {start, end} = dateRange.value;
      const res = await get(`/stats/daily`);
      dailyConfig.value.data = spanDateRange(start, end, res?.data || [], 'date');
    };

    const getTasks = async () => {
      // TODO: filter by date range?
      const {start, end} = dateRange.value;
      const res = await get(`/stats/tasks`);
      tasksByStatusConfig.value.data = res?.data.by_status;
      tasksByNodeConfig.value.data = res?.data.by_node;
      tasksBySpiderConfig.value.data = res?.data.by_spider;
    };

    const getData = async () => Promise.all([
      getOverview(),
      getDaily(),
      getTasks(),
    ]);

    const onMetricClick = (m: MetricMeta) => {
      if (m.path) {
        router.push(m.path);
      }
    };

    const defaultColorFunc = (value: string | number) => {
      if (typeof value === 'number') {
        // number
        if (value === 0) {
          return getColorByKey('info-medium');
        } else {
          return getColorByKey('primary');
        }
      } else {
        // string
        const v = Number(value);
        if (isNaN(v) || v == 0) {
          return getColorByKey('info-medium');
        } else {
          return getColorByKey('primary');
        }
      }
    };

    const getColor = (m: MetricMeta) => {
      if (!m.color) {
        return defaultColorFunc(m.value);
      } else if (typeof m.color === 'function') {
        return m.color(m);
      } else {
        return m.color;
      }
    };

    onMounted(async () => {
      await getData();
    });

    return {
      metrics,
      dailyConfig,
      tasksByStatusConfig,
      tasksByNodeConfig,
      tasksBySpiderConfig,
      onMetricClick,
      getColor,
    };
  },
});
</script>

